class YOLO11n_Set(ModelInfoSet):
    """Configuration set for YOLO11n models."""

    COCO_V1 = ModelInfo(
        model_cfg={
            "repo_id": "mobilint/YOLO11n",
            "filename": "yolo11n.mxq",
        },
        pre_cfg={
            "Reader": {
                "style": "numpy",
            },
            "YoloPre": {
                "img_size": [640, 640],
            },
            "SetOrder": {"shape": "HWC"},
        },
        post_cfg={
            "task": "object_detection",
            "nc": 80,  # Number of classes
            "nl": 3,  # Number of detection layers
            "reg_max": 16,
        },
    )
    DEFAULT = COCO_V1


